    private void updateCategoryTable(ArrayList<Category> discountedCategories) {
        DefaultTableModel model = (DefaultTableModel) categoryTable.getModel();
        model.setRowCount(0);
        try {
            ArrayList<Category> categories = pos.getInventory().getCategories();
            for (Category category : categories) {
                boolean discounted = false;
                    if (discountedCategories.contains(category)) {
                        discounted = true;
                    }
                Object[] rowData = {
                    category.getId(),
                    category.getName(),
                    discounted};
                model.addRow(rowData);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void updateVariantsTable(ArrayList<ProductVariant> products) {
        DefaultTableModel model = (DefaultTableModel) productTable.getModel();
        model.setRowCount(0);

        try {
            for (Product product : pos.getInventory().getProducts()) {
                for (ProductVariant variant : product.getVariants()) {
                    boolean discounted = false;
                    if (products.contains(variant)) {
                        discounted = true;
                    }
                    Object[] rowData = {
                        variant.getVariantID(),
                        product.getID(),
                        product.getName(),
                        variant.getSize(),
                        variant.getColour(),
                        variant.getPrice(),
                        variant.getQuantity(),
                        variant.getMinQuantity(),
                        discounted
                    };
                    model.addRow(rowData);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

